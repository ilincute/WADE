package com.mexservice.mex.data;

public class ExponateData
{
    String id;
    String exponateId;
    String name;
    String value;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getExponateId() {
        return exponateId;
    }

    public void setExponateId(String exponateId) {
        this.exponateId = exponateId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
